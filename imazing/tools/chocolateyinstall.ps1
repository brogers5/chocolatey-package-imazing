$packageName = 'imazing'
$installerType = 'exe'
$silentArgs = '/VERYSILENT'
$toolsDir     = "$(Split-Path -parent $MyInvocation.MyCommand.Definition)"
$url = 'https://downloads.imazing.com/windows/iMazing/2.16.7/iMazing_2.16.7.exe'
$checksum = 'fa05a3b0f33a44bf9aa71d7baac19a5aa23ebd929d2ac1ee6bd7e7898e591dff'
$checksumType = 'sha256'
$validExitCodes = @(0)
 
Install-ChocolateyPackage -PackageName "$packageName" `
                          -FileType "$installerType" `
                          -SilentArgs "$silentArgs" `
                          -Url "$url" `
                          -ValidExitCodes $validExitCodes `
                          -Checksum "$checksum" `
                          -ChecksumType "$checksumType"
